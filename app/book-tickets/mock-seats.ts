export function generateMockRows() {
    const rows = []
    const SEAT_STATUS = ["reserved" , "available"]
    for (let index = 0; index < 10; index++) {
        if(index === 6) {
            rows.push(null)
            continue
        }

        const row : {name:string,seats:any[]} = {
            name: String.fromCharCode(65 + index),
            seats :[] 
        }
        
        for (let j = 0; j < 20; j++) {
            if(j === 10) {
                row.seats.push(null)
                continue
            }

            const seat = {
                id : Math.ceil(Math.random() * 1000000),
                name: `Seat ${index}-${j}`,
                status:  SEAT_STATUS[Math.round(Math.random() * 1)]
            }

            row.seats.push(seat)
        }
        
        rows.push(row)

    }
    return rows
}